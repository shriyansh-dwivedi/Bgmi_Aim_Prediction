import cv2
import math
from ultralytics import YOLO
from google.colab.patches import cv2_imshow

# Load model
model = YOLO("yolov8n.pt")

# Video path
video_path = "/content/Screen Recording 2026-04-30 152656.mp4"

cap = cv2.VideoCapture(video_path)

# Get video properties for VideoWriter
fps = cap.get(cv2.CAP_PROP_FPS)
width = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
height = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))

# Define the codec and create VideoWriter object
fourcc = cv2.VideoWriter_fourcc(*'mp4v') # Codec for .mp4 files
out = cv2.VideoWriter('output.mp4', fourcc, fps, (width, height))

while True:

    ret, frame = cap.read()

    if not ret:
        break

    # Frame size (using the current frame's dimensions, which should match video properties)
    h, w, _ = frame.shape

    # Crosshair center
    crosshair_x = w // 2
    crosshair_y = h // 2

    # Draw crosshair
    cv2.circle(frame, (crosshair_x, crosshair_y), 8, (0,0,255), -1)

    # YOLO detect
    results = model(frame)

    for box in results[0].boxes:

        cls = int(box.cls[0])

        # person class only
        if cls == 0:

            x1, y1, x2, y2 = map(int, box.xyxy[0])

            # enemy center
            enemy_x = (x1 + x2) // 2
            enemy_y = (y1 + y2) // 2

            # Draw enemy center
            cv2.circle(frame, (enemy_x, enemy_y), 8, (255,0,0), -1)

            # Distance formula
            distance = math.sqrt(
                (enemy_x - crosshair_x)**2 +
                (enemy_y - crosshair_y)**2
            )

            # AIM STATUS
            if distance < 100:
                text = "GOOD AIM"
                color = (0,255,0)

            else:
                text = "BAD AIM"
                color = (0,0,255)

            # Draw box
            cv2.rectangle(frame, (x1,y1), (x2,y2), color, 2)

            # Show text
            cv2.putText(
                frame,
                text,
                (x1, y1 - 10),
                cv2.FONT_HERSHEY_SIMPLEX,
                1,
                color,
                2
            )
    # Write the processed frame to the output video
    out.write(frame)

    # Show frame (optional, if you want to see the output in real-time in Colab)
    cv2_imshow(frame)

    # Exit with Q
    if cv2.waitKey(1) & 0xFF == ord('q'):
        break

cap.release()
out.release() # Release the VideoWriter object as well
cv2.destroyAllWindows()

